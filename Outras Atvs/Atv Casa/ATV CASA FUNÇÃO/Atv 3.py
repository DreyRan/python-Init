# Função Soma Simples
# Crie uma função chamada soma que:
# Receba dois parâmetros numéricos
# Imprima o resultado da soma entre eles
# Não utilize return

def soma(Num1,Num2):
    print( " Soma: ",Num1 + Num2,)
    print( " Multiplicação:", Num1 * Num2)
    print( " Divisão: ", Num1 / Num2)
    print( " Subitração: ", Num1 - Num2)
    print( " Exponenciação: ", Num1 * Num2 ** 2)




Num1 = float(input("qual o 1 numero?"))
Num2 = float(input("Qual o 2 numero?"))

soma(Num1,Num2)